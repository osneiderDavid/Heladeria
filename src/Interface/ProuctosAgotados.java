    void ListarAgotado(){
        try{
        List<Producto> Lista = Procesos.ListarAgotados();
        Tabla = (DefaultTableModel)tabla_Contenido.getModel();
        Object [] Code = new Object[6];
       
        for(int i = 0; i<Lista.size(); i++){
            
            Code[0] = Lista.get(i).getId();
            Code[1] = Lista.get(i).getSabores();
            Code[2] = Lista.get(i).getDescripcion();
            Code[3] = Lista.get(i).getPrecio();
            Code[4] = Lista.get(i).getDescuento();
            Code[5] = Lista.get(i).getEstado();
           
            Tabla.addRow(Code);
        }
       tabla_Contenido.setModel(Tabla);
        }catch(Exception error){
            System.out.println("Error en Listar: "+error);
        }
    }
