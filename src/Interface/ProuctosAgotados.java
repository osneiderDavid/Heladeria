    private void tabla_ContenidoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla_ContenidoMouseClicked
        // TODO add your handling code here:
         try{
        int filas = tabla_Contenido.getSelectedRow();
        
        if(filas == -1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
        } else{    
            
            String Estado1;
          Id      = Integer.parseInt(tabla_Contenido.getValueAt(filas, 0).toString());
          Estado1 = tabla_Contenido.getValueAt(filas, 5).toString();
          est.setSelectedItem(Estado1);
        }
        }catch(HeadlessException error){
            System.out.println("Error en Seleccionar Lista: "+error);
        }
    }//GEN-LAST:event_tabla_ContenidoMouseClicked

    
    void Limpiar(){
        sab.setText("");
        descrip.setText("");
        pre.setText("");
        desc.setText("");
        cant.setText("");
    }
    
    void ModificarAgotados(){
        try{
         
         String Estado = est.getSelectedItem().toString();
        
         Object[] Agotado = new Object[2];
         
         Agotado[0] = Estado;
         Agotado[1] = Id;
         Procesos.ModificarAgotados(Agotado);
        
        }catch(Exception error){
            System.out.println("Error en Modificar Agotados: "+error);
        }
    }
    
    void VaciarTabla(){
        for(int i = 0; i<Tabla.getRowCount(); i++){
            Tabla.removeRow(i);
             i=i-1;
        }
    }
 
