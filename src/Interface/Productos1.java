    void Registrar(){
        try{
            String Sabores     = sab.getText();
            String Descripcion = descrip.getText();
            double  Precio     = Double.parseDouble(pre.getText());
            String Descuento   = desc.getText();
            int    Cantidad    = Integer.parseInt(cant.getText());
            String Estado      = est.getSelectedItem().toString();
            
            Object [] Registro = new Object[6];
            
            Registro[0] = Sabores;
            Registro[1] = Descripcion;
            Registro[2] = Precio;
            Registro[3] = Descuento;
            Registro[4] = Cantidad;
            Registro[5] = Estado;
            
            Operaciones.Ingresar(Registro);
            
        }catch(Exception error){
            System.out.println("Error en Registrar: "+error);
        }
    }

    void VaciarTabla(){
        for(int i = 0; i<Tab.getRowCount(); i++){
            Tab.removeRow(i);
             i=i-1;
        }
    }
    
