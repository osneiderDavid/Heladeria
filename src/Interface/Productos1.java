    void Listar(){
        try{
        List<Producto> Lista = Operaciones.Listado();
        Tab = (DefaultTableModel)tabla_Contenido.getModel();
        Object [] List = new Object[6];
       
        for(int i = 0; i<Lista.size(); i++){
            
            List[0] = Lista.get(i).getId();
            List[1] = Lista.get(i).getSabores();
            List[2] = Lista.get(i).getDescripcion();
            List[3] = Lista.get(i).getPrecio();
            List[4] = Lista.get(i).getDescuento();
            List[5] = Lista.get(i).getCantidad();
           
            Tab.addRow(List);
        }
       tabla_Contenido.setModel(Tab);
        }catch(Exception error){
            System.out.println("Error en el Listado: "+error);
        }
    }
